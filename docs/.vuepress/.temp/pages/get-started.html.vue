<h2 id="автоприем-заказов" tabindex="-1"><a class="header-anchor" href="#автоприем-заказов"><span>Автоприём заказов</span></a></h2>
<ul>
<li>Автоприём повышает приоритет при распределении заказов.</li>
<li>Позволяет не пропускать новые назначения и не тратить время на подтверждение, если вы в активном заказе.</li>
<li>Оповещения о заказах приходят, как и прежде, а сами заказы сразу появляются в списке.</li>
</ul>
<h3 id="активация-функции" tabindex="-1"><a class="header-anchor" href="#активация-функции"><span>Активация функции</span></a></h3>
<ul>
<li>В активной смене перейдите в личный кабинет Шоппера.</li>
<li>Зайдите в раздел «Автоприём заказов».</li>
<li>Включите «Принимать все заказы».</li>
<li>Управление функцией доступно во время смены: можно включать и отключать её по своему усмотрению.</li>
</ul>
<p>Обратите внимание на информацию при новом заказе:</p>
<ul>
<li>Вес заказа.</li>
<li>Время подлёта.</li>
<li>Время назначения.</li>
</ul>
<p>Возможность непринятия заказа без корректировки дохода:</p>
<ul>
<li>
<p>Вес заказа более 15 кг (для пеших и велокурьеров).</p>
</li>
<li>
<p>Время подлёта более 25 минут.</p>
</li>
<li>
<p>Назначение пришло за 5 минут до планового завершения слота или после него.</p>
</li>
<li>
<p>Даже с включённым автоприёмом заказов можно не принимать заказ на вышеуказанных условиях.</p>
</li>
<li>
<p>Отказ от уже принятого заказа требует веских оснований, как и с любым другим заказом.</p>
</li>
</ul>
<h2 id="повышенныи-спрос-и-карта-повышенного-спроса" tabindex="-1"><a class="header-anchor" href="#повышенныи-спрос-и-карта-повышенного-спроса"><span>Повышенный спрос и карта повышенного спроса</span></a></h2>
<ul>
<li>Карта показывает наличие свободных исполнителей в непосредственной близости от каждого конкретного магазина.</li>
<li>Чем темнее зона, тем меньше курьеров и больше вероятность, что в ней есть нераспределенные заказы, но их наличие не гарантируется.</li>
<li>Высокая загруженность курьеров в каждой конкретной зоне:
<ul>
<li>Повышает минимальную корзину для клиентов.</li>
<li>Увеличивает стоимость доставки для магазинов.</li>
<li>Для ресторанов уменьшает зону доставки, увеличивает время доставки (например, вместо 15-30 минут может быть 90-120 минут).</li>
</ul>
</li>
<li>Высокая нагрузка на сервис ограничивает клиентов в заказах, снижая количество заказов.</li>
<li>Бонус рассчитывается исходя из загруженности сервиса.
<ul>
<li>Если курьер переместился в зону повышенного спроса, но ему назначен заказ из точки без повышенного спроса, бонус не начисляется.</li>
<li>Бонус рассчитывается индивидуально для каждого участка и меняется каждую минуту.</li>
</ul>
</li>
<li>У части курьеров на карте спроса также отражён таймер.
<ul>
<li>Таймер не влияет на получение доплаты, а показывает время до следующего обновления карты.</li>
<li>Если спрос в зоне закончился к моменту назначения заказа, бонус за спрос не начисляется.</li>
</ul>
</li>
</ul>
<h2 id="назначение-заказов-и-таимеры" tabindex="-1"><a class="header-anchor" href="#назначение-заказов-и-таимеры"><span>Назначение заказов и таймеры</span></a></h2>
<ul>
<li>Для ресторанов, Пятерочек и Перекрестка учитывается время, необходимое на приготовление и сборку.</li>
