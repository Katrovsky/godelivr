<h3 id="рестораны" tabindex="-1"><a class="header-anchor" href="#рестораны"><span>Рестораны</span></a></h3>
<ul>
<li>Каждый ресторан заявляет время приготовления (например, 20 минут).</li>
<li>В момент оформления заказа система смотрит доступных курьеров.</li>
</ul>
<p><strong>Вариант 1</strong></p>
<ul>
<li>Есть курьер, который доберется до ресторана через 5 минут.</li>
<li>Заказ отдается в ресторан, и через 15 минут заказ передается курьеру.</li>
</ul>
<p><strong>Вариант 2</strong></p>
<ul>
<li>Есть курьер, который доберется до ресторана через 45 минут.</li>
<li>Заказ отдается курьеру, и через 25 минут заказ передается в ресторан.</li>
</ul>
<h3 id="пятерочки-и-перекрестки" tabindex="-1"><a class="header-anchor" href="#пятерочки-и-перекрестки"><span>Пятерочки и Перекрестки</span></a></h3>
<ul>
<li>Ритейлер предоставляет данные о прогнозируемом времени готовности заказа, на которое система ориентируется при распределении.</li>
</ul>
<h3 id="доступность-сервиса" tabindex="-1"><a class="header-anchor" href="#доступность-сервиса"><span>Доступность сервиса</span></a></h3>
<ul>
<li>Сервис доступен для оформления заказов до 22:00.
<ul>
<li>Если клиент оформит заказ в 21:50, курьеры со сменами до 22:00 его не доставят.</li>
<li>На часы 22:00-23:00 прогнозируется 1 курьер, не больше.</li>
</ul>
</li>
</ul>
<h2 id="добавочныи-код-для-звонка-клиенту" tabindex="-1"><a class="header-anchor" href="#добавочныи-код-для-звонка-клиенту"><span>Добавочный код для звонка клиенту</span></a></h2>
<p>В Шоппере в информации о клиенте вы видите номер телефона и добавочный код к нему.
Для оповещения клиента о времени доставки и уточнения деталей по адресу в случае сомнений, необходимо позвонить ему перед доставкой.</p>
<ul>
<li>
<p>Чтобы позвонить клиенту с устройства, на котором у вас установлен Шоппер, нажмите на иконку звонка, как обычно. Если для звонка клиенту вы используете другой телефон, введите номер, далее запятую (долгое нажатие на *) и код клиента.</p>
</li>
<li>
<p>Позвонить без добавочного кода или написать на указанный номер невозможно — сообщение не будет доставлено клиенту.</p>
</li>
</ul>
